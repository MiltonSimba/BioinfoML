#In Python code  is contained in a module, you can import a math module containing reusable code
import math      #instead of memorising all math functions, we can use the maath module easily

x=2.9     #to round the number we use the round() math function
print(round(x))
print(abs(-2))         #aboslute function always returns  the positive number
 
#Using the math module we can call math function/methods by typing math. and leave the suggestion to appear
print(math.ceil(x))     #math.ceil  function returns the  next big integer for a given number, unlike round which returns a specified number of decimals.
print(math.floor(x))     #returns the biggest smaller integer to a given number